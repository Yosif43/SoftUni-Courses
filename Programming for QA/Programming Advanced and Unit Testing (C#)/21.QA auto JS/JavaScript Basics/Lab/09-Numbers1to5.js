//Declare function
function printNumbers1to5 () {

    //For цикъл
    //повторение: отпечатваме дадено число
    //начало: 1
    //край: 5
    //промяна: +1

    for (let number = 1; number <= 5; number++) {
        //действие, което повтаряме
        console.log(number);
    }
}

//Invokation function
printNumbers1to5();
